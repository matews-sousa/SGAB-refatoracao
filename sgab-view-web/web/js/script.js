
// Formulário de Cadastro
function abreFormCadastro() {
  document.getElementById("formCadastro").style.display = "block";
}

function fechaFormCadastro() {
  document.getElementById("formCadastro").style.display = "none";
}


//Formulário de Edição
function abreFormEditar() {
  document.getElementById("formEdicao").style.display = "block";
}

function fechaFormEditar() {
  document.getElementById("formEdicao").style.display = "none";
}

function abreFormEdicaoConfirmada() {
  document.getElementById("formEdicao").style.display = "none";
  document.getElementById("formEdicaoConfirmada").style.display = "block";
}

function fechaFormEdicaoConfirmada() {
  document.getElementById("formEdicaoConfirmada").style.display = "none";
}

